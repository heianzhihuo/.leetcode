/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    int in,pre;
    public TreeNode buildTree(int[] preorder, int[] inorder) {
        in = 0;
        pre = 0;
        return buildTree(preorder,inorder,Integer.MAX_VALUE);
    }

    public TreeNode buildTree(int[] preorder,int[] inorder,int rootVal){
        if(pre==preorder.length || inorder[in]==rootVal) return null;
        TreeNode root = new TreeNode(preorder[pre]);
        pre++;
        root.left = buildTree(preorder,inorder,root.val);
        in++;
        root.right = buildTree(preorder,inorder,rootVal);
        return root;
    }
}
